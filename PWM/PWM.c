#include "PWM.h"

//----------------------------------------------------

void init_PWM_timer(void)
{
	ASSR  = 0x00;
	TCCR2 = 0b01101110;		// fast PWM ON, clear OC2 on Compare Match, clk T2S/256 (From Prescaler) (реальная частота получится 8мгц/256 = 31250гц)
	TCNT2 = 0x00;			// Установка отсчета таймера с нуля
	OCR2  = 0;				// установка скважности 0
	TIMSK = 0x00;			// 
	
	
}

